# Turtle is what is used to make the square.
import turtle
t = turtle.Turtle()
r = int(input("Enter how long you want the square to be:"))
t.forward(r)
t.left(90)
t.forward(r)
t.left(90)
t.forward(r)
t.left(90)
t.forward(r)
t.left(90)
